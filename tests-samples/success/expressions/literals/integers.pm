# Basic Integers
println(1 == 01)
println(2 == 000002)
println(1_234_234 == 1234234)
println(0001 == 1)
# Hex Integers
println(0xF2ac6A == 15903850)
println(0XA == 10)
println(0xbac == 2988)
# Octal Integers
println(0o142 == 98)
println(0O777 == 511)
println(0o0001 == 1)
# Binary Integers
println(0b010101 == 21)
println(0B111 == 7)
println(0b001 == 1)
